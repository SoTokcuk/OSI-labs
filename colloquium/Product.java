public abstract class Product {
    private int weight;
    private int price;
    
    public void setWeight(int weight)
    {
        this.weight = weight;
    }

    public void setPrice(int price)
    {
        this.price = price;
    }

    public int getWeight()
    {
        return this.weight;
    }

    public int getPrice()
    {
        return this.price;
    }

}
